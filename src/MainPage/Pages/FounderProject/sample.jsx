const getNoOfCycles = (start_date, end_date) => { // project cycle calculation
  let s_date = new Date(start_date);
  let e_date = new Date(end_date);
  let months = (e_date.getFullYear() - s_date.getFullYear()) * 12;
  months -= s_date.getMonth();
  months += e_date.getMonth();
  return months <= 0 ? 1 : Math.round(months / 3) || 1;
}





